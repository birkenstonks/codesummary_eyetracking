public class IndexToLetterConverter {

    public String indexToLetter(int index) {
        if (index == 0)
            return "a";
        else if (index == 1)
            return "b";
        else if (index == 2)
            return "c";
        else if (index == 3)
            return "d";
        else if (index == 4)
            return "e";
        else if (index == 5)
            return "f";
        else if (index == 6)
            return "g";
        else if (index == 7)
            return "h";
        else if (index == 8)
            return "i";
        else if (index == 9)
            return "j";
        else
            return null;
    }
}